# Arithmitic Operators

a = 10
b = 20
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)


# Comparison Operators

x = 5
y = 10

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

# Logical Operators

x = 8
y = 6
b = "logical AND"
print(b)
print(x < y and y < x)
print(y < x and x > y)
print(x != y and x > y)

b = "logical OR"
print(b)
print(x < y or y < x)
print(y > x or x < y)
print(x != y or x < y)

b = "logical NOT"
print(b)
print(not x < y)
print(not y < x)
print(not x != y)


