# Data Type in Python

# 1. String
first_Name = "sakeena";
print(type(first_Name));

# 2. floating point numbers
screen_size = 4.5;
print(type(screen_size));

# 3. Integer
score = 90;
print(type(score));

# 4. Boolean
is_maried = False;
print(type(is_maried));

# 5. None
age = None;
print(type(age));